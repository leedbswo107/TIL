/**
 * 문제
 * 싱그러운 어느 날, 헤이(Hey)는 브실이와 카페에서 칼바도스(Calvados)를 마시며 담소를 나누고 있다.
 * 브실: "먹고살기 바빠서 너무 오랜만에 보는 거 같아..."
 * 헤이: "그러게, 요즘 물가도 많이 오르고 뭐 하나 쉽게 사 먹기도 힘들어졌어.
 * "브실: "어제 시장에 갔는데 맘 놓고 사질 못했어. 배도 비싸고, 사과도 3개 12,000원이나 하던걸..."
 * 헤이: "아?! 사과 얘기를 해서 생각난 건데 말이지, 집에서 나오려고 하는데..."이야기의 주제는 월급 빼고 다 오르는 물가 상승과...,무엇보다도 남편 두식(Doo-Sik)이의 아빠가 보낸 '사과상자'와 배즙 상자 때문에 현관문이 막혀 갇힐 뻔하여 카페에 못 올 뻔한 것!!
 * (상자로 현관문이 막힌 상황의 그림)
 * 헤이: "두식이 아빠가 보낸, (우주 그 어디서도 맛볼 수 없는, 새로운 사과의 지평선을 개척하기 시작한) '군위윤하사과'는...독특한 재배 방식으로 가로, 높이, 세로 12cm 정육면체 모양으로, 질량 500g으로 일정하고, 포장 또한 질량 1,000g짜리 특수 제작 상자로 안전하게 배송하고 있어.이것저것 보내는 거 좋아하시는 아버님이 많이 안 보냈다는데, 같이 보낸 배즙 상자에는 어디서 구매한 건지 알 수 없는 배즙이 가득 들어 있더라고."
 * 헤이는 문을 열 수 없게 만들었던 '사과상자'를 열심히 설명했고, 브실이는 문을 막고 있는 상자들이 얼마나 무거운지 문득 궁금해졌다.상자의 개수와 종류, 크기 정보가 주어질 때, 문을 막고 있는 상자의 질량을 계산해 보자.'사과상자'에는 사과가 상자의 변과 평행하게 최대한 많이 들어있고, 배즙 상자에는 크기와 상관 없이  120g짜리 배즙 50개씩 들어있다고 계산하고, 상자 자체의 질량은 무시하기로 하자.또한, '사과상자'에 현금이 들어 있었다면 얼마였을지 알 순 없지만, 현금을 좋아하는 헤이를 위해 사과의 값어치가 얼마인지를 시장 사과 가격으로 단순히 계산해 보자.
 * 
 * 출력
 * 첫 번째 줄에 문을 막고 있는 상자의 질량을 g 단위로 출력한다.두 번째 줄에 사과의 값어치가 얼마인지를 시장 사과 가격(원)으로 단순히 계산하여 출력한다.
 */
const [N, ...input] = require('fs').readFileSync('./input.txt', 'utf-8').trim().split('\n');
let result = new Array(2).fill(0);
const A = (w, h, l) => {
  const box = 1000;
  const d = 12;
  const W = 500;
  const price = 4000;
  const count = Math.floor(w / d) * Math.floor(h / d) * Math.floor(l / d);
  result[0] += (count * W + box);
  result[1] += (count * price);
}
input.forEach(e => {
  const [T, W, H, L] = e.split(' ');
  T === 'A' ? A(+W, +H, +L) : (result[0] += 6000);
});
console.log(result.join('\n'));